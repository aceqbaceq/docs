#!/bin/bash

l0_array=(0 1 2 )
B_array=("0 2 2 1 1" "2 2 1 1 0" ); B_size=${#B_array[@]}; B_last_el=$(( $B_size - 1  )) ;



for B in $(seq 0 1 $B_last_el); do

    for l0 in "${l0_array[@]}";do 
	A=( ${B_array[$B]}  ); A1="${A[0]}"; A2="${A[1]}"; A3="${A[2]}";   A4="${A[3]}"; A5="${A[4]}";

	P1=$( bc <<<  " $l0*0.37+$A1*0.67       "           )
	P2=$( bc <<<  " $P1*0.37+$A2*0.67       "           )
	P3=$( bc <<<  " $P2*0.37+$A3*0.67       "           )
	P4=$( bc <<<  " $P3*0.37+$A4*0.67       "           )
	P5=$( bc <<<  " $P4*0.37+$A5*0.67       "           )

	printf " l0=%-2s | A=(%s) | P5=%s\n"  "$l0"  "${A[*]}"  "$P5"
    done

    echo "======================================"

done
